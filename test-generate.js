#!/usr/bin/env node

// Simplified version of the generate command for testing
const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

console.log('\n🚀 API-Doc-Sync - Simple Test Generator\n');

// Load the example spec
const specPath = path.join(__dirname, 'examples/petstore.yaml');

if (!fs.existsSync(specPath)) {
  console.error(`❌ Spec file not found: ${specPath}`);
  process.exit(1);
}

console.log(`📖 Loading spec from: ${specPath}`);
const specContent = fs.readFileSync(specPath, 'utf8');
const spec = yaml.load(specContent);

console.log(`✅ Loaded: ${spec.info.title} v${spec.info.version}`);
console.log(`📊 Paths: ${Object.keys(spec.paths).length}`);
console.log(`🏷️  Tags: ${spec.tags.length}`);
console.log(`📦 Schemas: ${Object.keys(spec.components.schemas).length}`);

// Create output directory
const outputDir = path.join(__dirname, 'test-output');
if (!fs.existsSync(outputDir)) {
  fs.mkdirSync(outputDir, { recursive: true });
}

// Generate README.md
let readme = `# ${spec.info.title}\n\n`;
readme += `**Version:** ${spec.info.version}\n\n`;
readme += `${spec.info.description || ''}\n\n`;

if (spec.info.contact) {
  readme += `**Contact:** ${spec.info.contact.name} (${spec.info.contact.email})\n\n`;
}

readme += `## Servers\n\n`;
for (const server of spec.servers) {
  readme += `- \`${server.url}\` - ${server.description}\n`;
}

readme += `\n## Documentation\n\n`;
readme += `- [API Reference](./API.md)\n`;
readme += `- [Schemas](./Schemas.md)\n\n`;

readme += `---\n\n*Generated by [api-doc-sync](https://github.com/Agolid/api-doc-sync)*\n`;

fs.writeFileSync(path.join(outputDir, 'README.md'), readme, 'utf8');
console.log(`✅ Generated: README.md`);

// Generate API.md
let apiDoc = `# API Reference\n\n`;

for (const [pathKey, pathItem] of Object.entries(spec.paths)) {
  apiDoc += `## ${pathKey}\n\n`;

  for (const [method, operation] of Object.entries(pathItem)) {
    if (!['get', 'post', 'put', 'delete', 'patch'].includes(method)) continue;

    const emojis = { get: '🔍', post: '📝', put: '✏️', delete: '🗑️', patch: '🩹' };
    apiDoc += `### ${emojis[method]} ${method.toUpperCase()}\n\n`;

    if (operation.summary) {
      apiDoc += `${operation.summary}\n\n`;
    }

    if (operation.description) {
      apiDoc += `${operation.description}\n\n`;
    }

    if (operation.operationId) {
      apiDoc += `**Operation ID:** \`${operation.operationId}\`\n\n`;
    }

    if (operation.parameters && operation.parameters.length > 0) {
      apiDoc += `#### Parameters\n\n`;
      apiDoc += `| Name | In | Type | Required | Description |\n`;
      apiDoc += `|------|-----|------|----------|-------------|\n`;

      for (const param of operation.parameters) {
        const type = param.type || param.schema?.type || 'unknown';
        const required = param.required ? 'Yes' : 'No';
        const description = param.description || '-';
        apiDoc += `| ${param.name} | ${param.in} | ${type} | ${required} | ${description} |\n`;
      }
      apiDoc += '\n';
    }

    if (operation.requestBody) {
      apiDoc += `#### Request Body\n\n`;
      if (operation.requestBody.description) {
        apiDoc += `${operation.requestBody.description}\n\n`;
      }
      apiDoc += `**Required:** ${operation.requestBody.required ? 'Yes' : 'No'}\n\n`;
    }

    if (operation.responses) {
      apiDoc += `#### Responses\n\n`;
      for (const [code, response] of Object.entries(operation.responses)) {
        apiDoc += `**${code}** - ${response.description}\n\n`;
      }
    }

    apiDoc += `---\n\n`;
  }
}

fs.writeFileSync(path.join(outputDir, 'API.md'), apiDoc, 'utf8');
console.log(`✅ Generated: API.md`);

// Generate Schemas.md
let schemasDoc = `# Data Schemas\n\n`;

for (const [name, schema] of Object.entries(spec.components.schemas)) {
  schemasDoc += `## ${name}\n\n`;

  if (schema.description) {
    schemasDoc += `${schema.description}\n\n`;
  }

  if (schema.type) {
    schemasDoc += `**Type:** ${schema.type}\n\n`;
  }

  if (schema.properties) {
    schemasDoc += `### Properties\n\n`;
    schemasDoc += `| Name | Type | Required | Description |\n`;
    schemasDoc += `|------|------|----------|-------------|\n`;

    for (const [propName, propSchema] of Object.entries(schema.properties)) {
      const type = propSchema.type || 'unknown';
      const required = schema.required?.includes(propName) ? 'Yes' : 'No';
      const description = propSchema.description || '-';
      schemasDoc += `| ${propName} | ${type} | ${required} | ${description} |\n`;
    }
    schemasDoc += '\n';
  }

  schemasDoc += `---\n\n`;
}

fs.writeFileSync(path.join(outputDir, 'Schemas.md'), schemasDoc, 'utf8');
console.log(`✅ Generated: Schemas.md`);

console.log(`\n🎉 Documentation generated successfully!`);
console.log(`📁 Output directory: ${outputDir}\n`);
console.log(`Generated files:`);
console.log(`  - README.md`);
console.log(`  - API.md`);
console.log(`  - Schemas.md\n`);
